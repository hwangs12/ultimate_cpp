/*
 * MULTITHREADING: multitasting feature that allows your
 * computer to run two or more programs concurrently.
 * There are two types: process-based and thread-based.
 *
 * Process-based multitasting handles the concurrent
 * execution of programs.
 *
 * Thread-based multitasking deals with the concurrent
 * pieces of the same program.
 *
 * Multithreaded program contains two or more parts
 * that can run concurrently.
 *
 * Each part of such a program is called a thread, and
 * each thread defines a separate path of execution.
 *
 */